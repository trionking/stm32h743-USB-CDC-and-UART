/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
	if (huart1.Instance->ISR & UART_FLAG_IDLE)
  {
		__HAL_UART_CLEAR_IDLEFLAG(&huart1);
		HAL_UART_AbortReceive(&huart1);
    uart1_stat.dma_rx_len = U1_RX_BUF_SIZE - __HAL_DMA_GET_COUNTER(&hdma_usart1_rx);
    Enqueue_bytes(&rx_UART1_queue, uart1_stat.rx_buffer, uart1_stat.dma_rx_len);
    uart1_stat.f_uart_rcvd = 1;
	  HAL_UART_Receive_DMA(&huart1, uart1_stat.rx_buffer, U1_RX_BUF_SIZE);
	}

  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

